    private Sheet leeHojaCalculo() {
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Archivos de Excel", "xls");
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileFilter(filtro);
        int resultArch = fileChooser.showDialog(jButtonGposCtes, "Cargar Archivo");
        if (resultArch == JFileChooser.APPROVE_OPTION) {
            File archivoCtes = fileChooser.getSelectedFile();
            Workbook workbook = null;
            try {
                workbook = Workbook.getWorkbook(archivoCtes);
            } catch (BiffException ex) {
                Logger.getLogger(CargaBancos.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                System.out.println("Error leyendo archivo de grupos");
            }
            hoja = workbook.getSheet(0);
        }
        return hoja;
    }
