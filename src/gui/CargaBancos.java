    private void jButtonCtesBcosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCtesBcosActionPerformed
        // TODO add your handling code here:
        Sheet hoja = leeHojaCalculo();
        int totFilas;
        totFilas = hoja.getRows();
        for (int fila = 1; fila < totFilas; fila++) {
            bancosClientes = new BancosClientes();
            Cell cellCte = hoja.getCell(0, fila);
            Cell cellBco = hoja.getCell(2, fila);
            Cell cellCta = hoja.getCell(3, fila);
            Cell cellMetodo = hoja.getCell(4, fila);
            bancosClientes.setCodigoCliente(cellCte.getContents());
            try {
                bancosClientes.setIdBanco(Integer.parseInt(cellBco.getContents()));
            } catch (NumberFormatException e) {
                bancosClientes.setIdBanco(0);
            }
            bancosClientes.setNumCtaPago(cellCta.getContents());
            bancosClientes.setMedioPago(cellMetodo.getContents());
            ctesBcos.add(bancosClientes);
        }
        try {
            DaoBancosSat.escribeBancosClientes(ctesBcos, bcos);
        } catch (SQLException ex) {
            Logger.getLogger(CargaBancos.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButtonCtesBcosActionPerformed
